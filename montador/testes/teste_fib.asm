; Imprime a sequência de fibonacci, até determinado limite.
; 12 36 9 37 33 9 38 34 13 34 10 33 1 34 11 35 2 36 7 30 13 35 9 34 33 9 35 34 5 10 13 36 14 0 0 0 0 0 1
SECAO	DATA
TEMP:	SPACE	3
LIMIT:	SPACE

SECAO	TEXT
MAIN:	INPUT	LIMIT
		COPY	ZERO,TEMP
		COPY	ONE,TEMP+1
		OUTPUT	TEMP+1
LOOP:	LOAD	TEMP
		ADD		TEMP+1
		STORE	TEMP+2
		SUB		LIMIT
		JMPP	BREAK
		OUTPUT	TEMP+2
		COPY	TEMP+1,TEMP
		COPY	TEMP+2,TEMP+1
		JMP		LOOP
BREAK:	OUTPUT	LIMIT
		STOP

SECAO	DATA
ZERO:	CONST	0
ONE:	CONST	1
