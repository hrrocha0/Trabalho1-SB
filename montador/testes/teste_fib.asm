; Imprime a sequência de fibonacci, até determinado limite.
; 12 40 5 7 13 40 14 9 41 38 13 38 10 37 1 38 11 39 2 40 7 35 13 39 9 38 37 9 39 38 5 15 5 4 0 0 0 0 0 1
SECAO	DATA
TEMP:	SPACE	3				; 37
LIMIT:	SPACE					; 40

SECAO	TEXT
MAIN:	INPUT	LIMIT			; 0
		JMP		FIB				; 2
RET:	OUTPUT	LIMIT			; 4
		STOP					; 6

FIB:	COPY	ZERO,TEMP		; 7
		COPY	ONE,TEMP+1		; 10
		OUTPUT	TEMP+1			; 13
LOOP:	LOAD	TEMP			; 15
		ADD		TEMP+1			; 17
		STORE	TEMP+2			; 19
		SUB		LIMIT			; 21
		JMPP	BREAK			; 23
		OUTPUT	TEMP+2			; 25
		COPY	TEMP+1,TEMP		; 27
		COPY	TEMP+2,TEMP+1	; 30
		JMP		LOOP			; 33
BREAK:	JMP		RET				; 35

SECAO	DATA
ZERO:	CONST	0				; 41
ONE:	CONST	1				; 42
